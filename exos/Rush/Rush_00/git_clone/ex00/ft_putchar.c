#include <unistd.h>

void	ft_putchar(int i)
{
	if (i == 0)
		write(1, "A", 1);
	if (i == 1)
		write(1, "B", 1);
	if (i == 2)
		write(1, " ", 1);
	if (i == 3)
		write(1, "C", 1);
	if (i == 4)
		write(1, "o", 1);
	if (i == 5)
		write(1, "-", 1);
	if (i == 6)
		write(1, "|", 1);
	if (i == 7)
		write(1, "\\", 1);
	if (i == 8)
		write(1, "/", 1);
	if (i == 9)
		write(1, "*", 1);
	if (i == 10)
		write(1, "\n", 1);
}
