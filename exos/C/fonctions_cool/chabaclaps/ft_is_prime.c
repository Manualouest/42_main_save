int	ft_is_prime(int nb)
{
	int	temp;

	temp = 2;
	if (nb < 2)
		return (0);
	while (temp <= nb / temp)
	{
		if ((nb % temp) == 0)
			return (0);
		temp ++;
	}
	return (1);
}
