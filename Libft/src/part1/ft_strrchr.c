#include <libft.h>

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = -1;
	while (s[++i])
		;
	i ++;
	while (--i >= 0 && s[i] != (char)c)
		;
	if (i >= 0)
		return (&((char *)s)[i]);
	return (NULL);
}
