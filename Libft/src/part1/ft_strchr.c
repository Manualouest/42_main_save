#include <libft.h>

char	*ft_strchr(const char *s, int c)
{
	int		i;

	i = -1;
	while (s[++i] && s[i] != (char)c)
		;
	if (s[i] == (char)c)
		return (&((char *)s)[i]);
	return (NULL);
}
